-- =====================================================================
--  Leave Management System — Secure Supabase schema
--  نظام إدارة الإجازات — مخطط قاعدة البيانات الآمن
-- =====================================================================
--  HOW TO RUN
--    Supabase Dashboard -> SQL Editor -> paste this whole file -> Run.
--    It is idempotent-ish: safe to re-run (uses IF NOT EXISTS /
--    CREATE OR REPLACE). It does NOT drop your data.
--
--  SECURITY MODEL (the important part)
--    * Row Level Security (RLS) is ON for every table. Nothing is
--      readable/writable by anonymous visitors — a login is required.
--    * The frontend uses ONLY the public "anon" JWT key. That key is
--      harmless on its own: RLS + these SECURITY DEFINER functions are
--      what actually protect the data.
--    * All sensitive writes (registering a deduction, editing an
--      employee, the JSON sync) go through SECURITY DEFINER functions
--      that re-check the caller's role and enforce every business rule
--      SERVER-SIDE. A malicious user with the anon key and full source
--      code STILL cannot bypass the 40-day rule, overdraw a balance,
--      or write to tables they shouldn't. The frontend checks are just
--      UX; Postgres is the real guard.
-- =====================================================================

