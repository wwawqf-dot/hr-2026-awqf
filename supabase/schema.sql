-- User profiles, 1:1 with Supabase Auth users. Holds the app role.
-- (Replaces the old `custom_users` idea: passwords live in Supabase
--  Auth, hashed and salted by Supabase — never in an app table.)
create table if not exists public.profiles (
    id         uuid primary key references auth.users(id) on delete cascade,
    username   text,
    role       text not null default 'data_entry' check (role in ('admin','data_entry','viewer')),
    created_at timestamptz not null default now()
);

create table if not exists public.years (
    id   bigint generated by default as identity primary key,
    year text unique not null
);

create table if not exists public.employees (
    id                      bigint generated by default as identity primary key,
    name                    text not null,
    job_number              text default '',
    national_id             text default '',
    job_title               text default '',
    initial_carried_forward numeric not null default 0,
    over_45                 boolean not null default false,
    is_frozen               boolean not null default false,
    hire_date               text default '',
    created_at              timestamptz not null default now()
);
-- Fast lookups used by the JSON sync bridge (match by job_number).
create index if not exists employees_job_number_idx
    on public.employees (job_number) where job_number <> '';

create table if not exists public.employee_years (
    id          bigint generated by default as identity primary key,
    employee_id bigint not null references public.employees(id) on delete cascade,
    year        text not null,
    added       numeric not null default 0,
    deducted    numeric not null default 0,
    unique (employee_id, year)
);

create table if not exists public.deductions (
    id          bigint generated by default as identity primary key,
    employee_id bigint not null references public.employees(id) on delete cascade,
    year        text not null,
    start_date  text default '',
    end_date    text default '',
    days        numeric not null,
    note        text,
    created_by  text,
    created_at  timestamptz not null default now()
);

create table if not exists public.audit_log (
    id        bigint generated by default as identity primary key,
    user_id   uuid,
    username  text,
    role      text,
    action    text,
    details   text,
    timestamp timestamptz not null default now()
);

create table if not exists public.settings (
    key   text primary key,
    value text
);

