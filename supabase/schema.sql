-- Internal audit writer. NOT granted to clients, so nobody can forge
-- audit entries directly — only our SECURITY DEFINER functions log.
create or replace function public.log_action(
    p_role text, p_username text, p_action text, p_details text
) returns void
language sql
security definer
set search_path = public
as $$
    insert into public.audit_log (user_id, username, role, action, details, timestamp)
    values (auth.uid(), p_username, p_role, p_action, p_details, now());
$$;

-- Serializes one employee to exactly the shape the frontend already
-- expects (mirrors the old assembleEmployee()).
create or replace function public.get_employee_json(p_id bigint)
returns jsonb
language sql
stable
security definer
set search_path = public
as $$
    select jsonb_build_object(
        'id', e.id,
        'name', e.name,
        'job_number', coalesce(e.job_number, ''),
        'national_id', coalesce(e.national_id, ''),
        'job_title', coalesce(e.job_title, ''),
        'initial_carried_forward', e.initial_carried_forward,
        'over_45', e.over_45,
        'is_frozen', e.is_frozen,
        'hire_date', coalesce(e.hire_date, ''),
        'years_data', coalesce((
            select jsonb_object_agg(ey.year,
                       jsonb_build_object('added', ey.added, 'deducted', ey.deducted))
            from public.employee_years ey where ey.employee_id = e.id
        ), '{}'::jsonb),
        'deductions_history', coalesce((
            select jsonb_agg(jsonb_build_object(
                       'id', d.id, 'year', d.year, 'start', d.start_date,
                       'end', d.end_date, 'days', d.days, 'note', d.note,
                       'createdBy', d.created_by, 'createdAt', d.created_at
                   ) order by d.id)
            from public.deductions d where d.employee_id = e.id
        ), '[]'::jsonb),
        'createdAt', e.created_at
    )
    from public.employees e where e.id = p_id;
$$;

-- Weekday day-count, faithful to backend/src/utils/days.js.
-- Postgres dow: 0=Sun .. 6=Sat (same mapping as JS getDay()).
-- Quran instructors ("محفظ"/"محفظة") observe Thu+Fri; everyone else Fri+Sat.
create or replace function public.calculate_deduction_days(
    p_start text, p_end text, p_custom_holidays numeric default 0, p_job_title text default ''
) returns numeric
language plpgsql
immutable
as $$
declare
    d_start date; d_end date; cur date; cnt int := 0; wd int; weekend int[];
begin
    if p_start is null or p_end is null or p_start = '' or p_end = '' then
        return 0;
    end if;
    begin
        d_start := p_start::date;
        d_end   := p_end::date;
    exception when others then
        return 0;
    end;
    if d_end < d_start then return 0; end if;

    if p_job_title in ('محفظ', 'محفظة') then weekend := array[4,5];
    else weekend := array[5,6]; end if;

    cur := d_start;
    while cur <= d_end loop
        wd := extract(dow from cur)::int;
        if not (wd = any(weekend)) then cnt := cnt + 1; end if;
        cur := cur + 1;
    end loop;

    return greatest(0, cnt - coalesce(p_custom_holidays, 0));
end;
$$;

